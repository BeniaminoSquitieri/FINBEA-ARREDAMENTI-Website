      Fondata da quattro giovani intraprendenti, Finbea consiste in una galleria d'arredamenti che permettono di rendere il proprio posto una vera
      e propria "casa".
      Contraddistinta dalla sua gamma di visioni e dal forte focus curatoriale, mette in mostra opere create da artisti di tutto
      il mondo e pezzi unici in edizione limitata. Ciò che accomuna Antonio, Fabrizio, Nicola e Beniamino - i quattro fondatori - è l'amore spropositato
      per l'arte e come questa possa appliccarsi in un contesto quotidiano come la propria abitazione arrivando in questo modo a chiunque. Questo
      obiettivo si raggiunge andando ad inserire le diverse tipologie di arredamenti esistenti: da uno stile più vintage a uno futuristico, passando
      per il rustico e il contemporaneo, da composizioni minimal ad altre più d'effetto.
      Finbea costituisce il futuro dell'arte e delle case, venendo anche citata in molte riviste legate all'argomento e dagli esperti del settore
      come Lindsey Adelman, Jason Miller e Piet Hein Eek.
      Abbiamo fatto tanta strada dalle nostre umili origini, ma la nostra visione strategica resta la stessa: creare una vita quotidiana migliore
      per la maggioranza delle persone. Nella storia di Finbea, le idee migliori sono sempre nate dalle sfide più grandi e crediamo che sia proprio
      questo a distinguerci dagli altri. Oggi siamo uno dei principali marchi di arredamento e sono circa duemila i lavoratori che realizzano
      insieme a noi la nostra visione strategica: creare una vita quotidiana migliore per la maggioranza delle persone.
      E questo è solo l’inizio: Il più rimane ancora da fare. Il futuro è meraviglioso!
